    private void setModel() {
        int fl2=0;
        
        for(Vendor ven:enterprise.getVendorDirectory().getVendorsArray()){
            
            for(UserAccount ua:ven.getUserAccountDirectory().getUserAccountList()){
                if(ua.getUsername().equals(account.getUsername())){
                    this.vendor = ven;
                    fl2=1;
                    break;
                }
            }
            if(fl2==1){
                break;
            }
        }
        
        ArrayList<Enterprise> countyEnterprises = new ArrayList();
        State net = null;
        int fl=0;
        for(State state: system.getStateList()){
            for(Enterprise ent: state.getEnterpriseDirectory().getEnterpriseListArray()){
                if(ent.getName() == enterprise.getName()){
                    fl=1;
                    net = state;
                    break;
                }
                if(fl==1){
                    break;
                }
            }
        }
        for(Enterprise ent:net.getEnterpriseDirectory().getEnterpriseListArray()){
            if(ent.getEnterpriseType().getValue().equals("County")){
                countyEnterpriseArray.add(ent);
            }
        }
    }
    
    
    
