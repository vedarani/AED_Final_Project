    private void populatePicker(){
        enterprisePicker.removeAllItems();
        int i=0;
        for (FNS fns: enterprise.getFnsDirectory().getFNS()) {
            if (i==0) {
                populateTable(fns);
                i++;
            }
            enterprisePicker.addItem(fns);
        }
    }
    
    private void populateTable(FNS fns) {
        DefaultTableModel model = (DefaultTableModel)tblFNS.getModel();
        model.setRowCount(0);
        
        String dm = "FNS Admin";
        
        for (UserAccount us: fns.getUserAccountDirectory().getUserAccountList()) {
            if (us.getRole().toString().equals(dm)) {
                Object[] row = new Object[2];
                row[0] = us;
                row[1] = us.getEmp().getName();
                model.addRow(row);
            }
            
        }
    }
    

