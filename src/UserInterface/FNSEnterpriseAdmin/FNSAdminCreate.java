    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        int selectedRow = tblFNS.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this,"Please select a row to delete","Warring",JOptionPane.WARNING_MESSAGE);
            return;
        }
        FNS fns = (FNS) enterprisePicker.getSelectedItem();

        DefaultTableModel model = (DefaultTableModel) tblFNS.getModel();

        UserAccount us = (UserAccount) model.getValueAt(selectedRow, 0);

        fns.getEmpDirectory().deleteEmp(us.getEmp());
        fns.getUserAccountDirectory().removeUser(us);
        populateTable(fns);
        JOptionPane.showMessageDialog(this,"FNS admin deleted successfully","Success",JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnDeleteActionPerformed

