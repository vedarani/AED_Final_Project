    private void saveData() {            

        String userName = txtUsername.getText();
        String password = txtPassword.getText();
        String name = txtFirstName.getText() + " " + txtLastName.getText();
        Long phoneNumber = Long.parseLong(txtPhoneNumber.getText());
        String email = txtEmailAddress.getText();
        String address = txtHomeAddress.getText();
        int age = Integer.parseInt(txtAge.getText());
        
        Emp employee = system.getEmpDirectory().createEmp(name);
        UserAccount useraccount = system.getUserAccountDirectory().createUserAccount(userName, password, employee, new ClientRole());
        
        Client client = system.getClientDirectory().createClient(age, employee, phoneNumber);
        client.setEmailAddress(email);
        client.setHomeAddress(address);
        
        dB4OUtil.storeSystem(system);
        JOptionPane.showMessageDialog(this,"Congratualtions... You took First Step towards fitness!!","Success",JOptionPane.INFORMATION_MESSAGE);
        navigateToLogin();
        
    }
    
    
