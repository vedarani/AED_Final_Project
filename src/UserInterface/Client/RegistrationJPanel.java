    // Common function to check validation for digits field
    private void allowOnlyDigitValidation(java.awt.event.KeyEvent evt) {
        char pressedKey = evt.getKeyChar();
        
        if(Character.isAlphabetic(pressedKey)) {
            evt.consume();
        }
    }
        
    // To check every validation of all fields
    private boolean isValidData() {
        boolean isValid = true;
        
        if (txtFirstName.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblFirstNameValidation, "Please enter first name");
            
        }
        if (txtLastName.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblLastNameValidation, "Please enter last name");
            
        }
        if (txtEmailAddress.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblEmailAddressValidation, "Please enter email address");
            
        } 
         
        if (txtAge.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblAgeValidation, "Please enter age");
            
        } 
        
        if (txtHomeAddress.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblHomeAddressValidation, "Please enter home address");
            
        } 
        
        if (txtPhoneNumber.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblPhoneNumberValidation, "Please enter Phone number");
            
        } else if (txtPhoneNumber.getText().length() != 10 || txtPhoneNumber.getText().startsWith("0")) {
            isValid = false;
            hideShowValidation(lblPhoneNumberValidation, "Please enter Phone Number in correct format");
            
        }
        
        if (txtUsername.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblUsernameValidation, "Please enter Username");
            
        } 
        /*else if (!UserAccountDirectory.checkIfUsernameIsUnique(txtUsername.getText())) {
            isValid = false;
            hideShowValidation(lblUsernameValidation, "Username already exists");
            
        }*/
        if (txtPassword.getText().isEmpty()) {
            isValid = false;
            hideShowValidation(lblPasswordValidation, "Please enter Password");
            
        } 
        
        return isValid;
    }
    
    // Common function to hide show validation labels
    private void hideShowValidation(JLabel lbl, String errorMessage) {
        if (errorMessage.isEmpty()) {
            lbl.setVisible(false);
        } else {
            lbl.setText(errorMessage);
            lbl.setVisible(true);
        }
        
    }
    
