    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to approve","Warring",JOptionPane.WARNING_MESSAGE);
            return;
        }
        FNSWorkApproval request = (FNSWorkApproval) tblProducts.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Waiting")) {
            request.setStatus("Approved");
            request.getProduce().setStatus("Approved");

            populateTable();
            JOptionPane.showMessageDialog(this, "Produce approved successfully","Sucess",JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Produce already approved or denied","Warring",JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnDenyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDenyActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to deny","Warring",JOptionPane.WARNING_MESSAGE);
            return;
        }
        FNSWorkApproval request = (FNSWorkApproval) tblProducts.getValueAt(selectedRow, 0);
        if (request.getStatus().equals("Waiting")) {
            request.setStatus("Denied");
            request.getProduce().setStatus("Denied");

            populateTable();
            JOptionPane.showMessageDialog(this, "Produce denied successfully","Success",JOptionPane.INFORMATION_MESSAGE);

        } else {
            JOptionPane.showMessageDialog(this, "Produce Already approved or denied","Warring",JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnDenyActionPerformed

    
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblProducts.getModel();
        model.setRowCount(0);
        for(Enterprise enterprise: countyEnterpriseArray){
            System.out.println(enterprise);
            for(County county: enterprise.getCountyDirectory().getCountyDirectoryArray()){
                ArrayList<WorkRequest> workReq = county.getWorkQueue().getWorkRequestListArray3();
                System.out.println(workReq);
                for(int i=workReq.size()-1; i>=0; i--){
                    FNSWorkApproval req = (FNSWorkApproval)workReq.get(i);
                    Object[] row = new Object[4];
                    row[0] = req;
                    row[1] = req.getProduce();
                    row[2] = req.getCounty();
                    row[3] = req.getStatus();
                    model.addRow(row);
                }
            }
        }
    }
    

