    private void btnSendResultsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendResultsActionPerformed

        int selectedRow = tblRequests.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to send results","Warring",JOptionPane.WARNING_MESSAGE);
            return;
        }

        request = (PathologyTestWR) tblRequests.getValueAt(selectedRow, 0);
        if(request.getStatus().equals("Test finished")){
            
            UserAccount ua = (UserAccount)dietitianPicker.getSelectedItem();
            request.setReceiver(ua);
            request.setStatus("Sent to Dietitian");
            populateTable();
            JOptionPane.showMessageDialog(this, "Test sent to Dietitian successfully","Success",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
                JOptionPane.showMessageDialog(this,"Request has been previously sent","Sucess",JOptionPane.INFORMATION_MESSAGE);
        }
        
        panelSendToDietitian.setVisible(false);
        panelTestResult.setVisible(false);
        populateTable();
    }//GEN-LAST:event_btnSendResultsActionPerformed

