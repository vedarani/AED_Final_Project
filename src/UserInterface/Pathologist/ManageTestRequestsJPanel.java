    private void populateDietitianJComboBox(){
        dietitianPicker.removeAllItems();
        
        String role = "Dietitian";
        
        for (UserAccount ua: fitnessCenter.getUserAccountDirectory().getUserAccountList()) {
            if (ua.getRole().toString().equals(role)) {
                dietitianPicker.addItem(ua);
            }
        }
    }
    
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblRequests.getModel();
        model.setRowCount(0);
        
        ArrayList<WorkRequest> workReq = fitnessCenter.getWorkQueue().getWorkRequestListArray2();
        for(int i=workReq.size()-1; i>=0; i--){
            
            PathologyTestWR lt = (PathologyTestWR)workReq.get(i);
            if (lt.getStatus().equals("Accepted") || lt.getStatus().equals("Rejected") || lt.getStatus().equals("Sample sent")) {
                continue;
            }
            
//            Produce product = lt.getProduce();
                Object[] row = new Object[5];
                row[0] = lt;
                row[1] = lt.getTest();
                row[2] = lt.getClient().getEmployee().getName();
                row[3] = lt.getStatus();
                String result = lt.getTestResult();
                row[4]  = result == null ? "Waiting" : result;
                model.addRow(row);
        }
    }
    
